package Week1.Learn.Variable;

public class VariableBase {
        double attack_;// 攻击力
        double hp_;// 血量
        double defense_;// 防御力
        double skill_;// 技能加成
        VariableBase(double attack, double hp, double defense, double skill) {
            attack_ = attack;
            hp_ = hp;
            defense_ = defense;
            skill_ = skill;
        }
}
